
// Operadores Aritméticos
let soma = 10 + 5;
let subtracao = 10 - 5;
let multiplicacao = 10 * 5;
let divisao = 10 / 5;
let modulo = 10 % 5;

// Operadores de Atribuição
let x = 10;
x += 5; // x = x + 5
x -= 5; // x = x - 5
x *= 5; // x = x * 5
x /= 5; // x = x / 5
x %= 5; // x = x % 5

// Operadores de Comparação
let igual = (10 == 5);
let estritamenteIgual = (10 === 5);
let diferente = (10 != 5);
let maiorQue = (10 > 5);
let menorQue = (10 < 5);
let maiorOuIgual = (10 >= 5);
let menorOuIgual = (10 <= 5);

// Operadores Lógicos
let e = (10 > 5) && (10 < 20);
let ou = (10 > 15) || (10 < 20);
let nao = !(10 > 5);

// Operadores de String
let concatenacao = "Hello" + " " + "World";

// Operadores Ternários
let resultado = (10 > 5) ? 'Maior' : 'Menor';

